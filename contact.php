$(document).ready(function(){
    $("#form1").validate({
        event: "blur",
              rules:{
          nombre:{
            required:true
          },
          confir:{
            required:true

          },
          correo:{
            required:true,
            email:true
          },
          mensaje:{
            required:true
          }
        },
        messages:{
          mensaje: 'Cuéntanos ¿Cómo podemos ayudarte?',
          nombre: 'Ingresa tus nombres y apellidos',
          correo: 'Ingresa tu correo electrónico',
          confir: 'Ingresa tu celular',
        },

        debug: true,errorElement: "label",
        submitHandler: function(form){
            $("#enviandoo").show();
            document.getElementById('enviar').style.display = 'none';
            $("#enviandoo").html("<strong>Enviando mensaje...</strong>");
            setTimeout(function() {
                $('#respuesta').fadeOut('slow');
            }, 5000);
            $.ajax({
                type: "POST",
                url:"send_contacto.php",
                data: "nombre="+($('#nombre').val())+"&confir="+($('#confir').val())+"&correo="+($('#correo').val())+"&mensaje="+($('#mensaje').val())+"&hora="+($('#hora').val()),
                success: function(msg){
                    $("#respuesta").html(msg);
                     document.getElementById('jumbotron').style.display = 'none';
                    document.getElementById("nombre").value="";
                    document.getElementById("confir").value="";
                    document.getElementById("correo").value="";
                    document.getElementById("mensaje").value="";
                    setTimeout(function() {
                        $('#respuesta').fadeOut('slow');
                    }, 5000);

                }
            });
        }
    });
});
